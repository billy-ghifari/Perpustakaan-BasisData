    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
        int duwekmuendicok = 250;

        try {
            DateFormat format = new SimpleDateFormat("dd MMMM yyyy");
            Date tanggalpinjam = format.parse(pinjam);
            Date tanggalkembali = format.parse(kembali);
            long tanggalpinjam1 = tanggalpinjam.getTime();
            long tanggalkembali1 = tanggalkembali.getTime();
            long diff = tanggalkembali1 - tanggalpinjam1;
            long lama = diff / (24 * 60 * 60 * 1000);
            lamapinjam.setText(Long.toString(lama) + "");

        } catch (Exception e) {
            System.out.println("" + e.getMessage());
        }

        int a = Integer.parseInt(lamapinjam.getText());
        int duwekkuakehcok = a * duwekmuendicok;

        if (duwekkuakehcok <= 0) {
            int x = 0;
            String y = Integer.toString(x);
            lamapinjam.setText(y);
            denda.setText(y);
        } else {
            String yay = Integer.toString(duwekkuakehcok);
            denda.setText(yay);

        }


    }//GEN-LAST:event_hitungActionPerformed

