    private void pilihan_pengembalian(String query) {
        kode_peminjaman.removeAllItems();
        kode_peminjaman.addItem("Pilih");
        try {
            java.sql.Connection conn = (Connection) koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(query);
            while (res.next()) {
                String a = res.getString("kode_peminjaman");
                kode_peminjaman.addItem(a);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "gagal Menampilkan Id Anggota " + e.getMessage());
        }

    }

