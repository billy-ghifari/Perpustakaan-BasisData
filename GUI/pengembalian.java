    private void load_tablePengembalian(String query) {
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Kode Peminjaman");
        model.addColumn("Kode Pengembalian");
        model.addColumn("Nama Anggota");
        model.addColumn("Kode Buku");
        model.addColumn("Judul Buku");
        model.addColumn("Tanggal Pinjam");
        model.addColumn("Tanggal Kembali");
        model.addColumn("Buku Kembali");
        model.addColumn("Telat");
        model.addColumn("Denda");

        //menampilkan data database kedalam tabel
        try {
            int no = 1;
            java.sql.Connection conn = (Connection) koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(query);
            while (res.next()) {
                //java.sql.Date sql=new java.sql.Date(res)

                model.addRow(new Object[]{no++, res.getString(1), res.getString(2), res.getString(3),
                    res.getString(4), res.getString(5), res.getString(6),
                    res.getString(7), res.getDate(8), res.getString(9), res.getString(10)});
            }
            jTable5.setModel(model);
        } catch (SQLException e) {
        }
    }

